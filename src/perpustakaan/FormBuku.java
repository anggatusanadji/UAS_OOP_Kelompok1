    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
        // TODO add your handling code here:
        String kode_buku=txtKodeBuku.getText();
        String judul_buku=txtJudulBuku.getText();
        String tahun=txtTahun.getText();
        String penulis=txtPenulis.getText();
        
        if (kode_buku.isEmpty() ) {
            JOptionPane.showMessageDialog(null,"Kode Buku tidak boleh kosong");
            txtKodeBuku.requestFocus();
        }else if (judul_buku.isEmpty()) {
            JOptionPane.showMessageDialog(null,"Judul Buku tidak boleh kosong");
            txtJudulBuku.requestFocus();
        }else if (tahun.isEmpty()) {
            JOptionPane.showMessageDialog(null,"Tahun Terbit tidak boleh kosong");
            txtTahun.requestFocus();
        }else if (penulis.isEmpty()) {
            JOptionPane.showMessageDialog(null,"Penulis tidak boleh kosong");
            txtPenulis.requestFocus();
        }else{
            try{
                Connection conn=(Connection)KoneksiDB.koneksiDB();
                Statement stt=conn.createStatement();
                stt.executeUpdate("insert into tb_buku(kode_buku,judul_buku,tahun_terbit,penulis)"+
                    "VALUES('"+kode_buku+"','"+judul_buku+"','"+tahun+"','"+penulis+"')");
                BersihData();
                refreshTable();               
                JOptionPane.showMessageDialog(this,"Data berhasil disimpan","Success",JOptionPane.INFORMATION_MESSAGE);
            } catch(SQLException e){
                JOptionPane.showMessageDialog(this,"Simpan data gagal\n"+e.getMessage(),"Error",JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnTambahActionPerformed

    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        // TODO add your handling code here:
        String kode_buku=txtKodeBuku.getText();
        String judul_buku=txtJudulBuku.getText();
        String tahun=txtTahun.getText();
        String penulis=txtPenulis.getText();
        
        if (kode_buku.isEmpty() ) {
            JOptionPane.showMessageDialog(null,"Id mapel tidak boleh kosong");
            txtKodeBuku.requestFocus();
        }else if (judul_buku.isEmpty()) {
            JOptionPane.showMessageDialog(null,"Nama mapel tidak boleh kosong");
            txtJudulBuku.requestFocus();
        }else if (tahun.isEmpty()) {
            JOptionPane.showMessageDialog(null,"Tahun Terbit tidak boleh kosong");
            txtTahun.requestFocus();
        }else if (penulis.isEmpty()) {
            JOptionPane.showMessageDialog(null,"Penulis tidak boleh kosong");
            txtPenulis.requestFocus();
        }else{
            try{
                Connection conn=(Connection)KoneksiDB.koneksiDB();
                Statement stt=conn.createStatement();
                int row = tbBuku.getSelectedRow();
                String value = (tbBuku.getModel().getValueAt(row, 0).toString());
                stt.executeUpdate("UPDATE tb_buku SET kode_buku='"+kode_buku+"', judul_buku='"+judul_buku+"', tahun_terbit='"+tahun+"', penulis='"+penulis+"' WHERE id='"+value+"'");
                BersihData();
                refreshTable();   
                btnTambah.setEnabled(true);
                JOptionPane.showMessageDialog(this,"Data berhasil diubah","Success",JOptionPane.INFORMATION_MESSAGE);
            } catch(SQLException e){
                JOptionPane.showMessageDialog(this,"Ubah data gagal\n"+e.getMessage(),"Error",JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnEditActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        // TODO add your handling code here:
        String kode_buku=txtKodeBuku.getText();

        if (kode_buku.isEmpty() ) {
            JOptionPane.showMessageDialog(null,"Kode buku tidak boleh kosong");
            txtKodeBuku.requestFocus();
        }else if(JOptionPane.showConfirmDialog(null,"Apakah anda yakin akan menghapus data ini?",
            "Informasi",JOptionPane.OK_CANCEL_OPTION,JOptionPane.INFORMATION_MESSAGE)==JOptionPane.OK_OPTION){
        try{
            Connection conn=(Connection)KoneksiDB.koneksiDB();
            Statement stt=conn.createStatement();
            stt.executeUpdate("DELETE FROM tb_buku WHERE kode_buku='"+kode_buku+"'");
            BersihData();
            refreshTable();    
            btnTambah.setEnabled(true);
            JOptionPane.showMessageDialog(this,"Data berhasil di hapus","Success",JOptionPane.INFORMATION_MESSAGE);
        } catch(SQLException e){
            JOptionPane.showMessageDialog(this,"Delete data gagal\n"+e.getMessage(),"Error",JOptionPane.ERROR_MESSAGE);
        }
        }
    
    }//GEN-LAST:event_btnHapusActionPerformed

