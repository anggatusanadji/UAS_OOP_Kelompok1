    private void refreshTable(){
        try{
            int total = 0;
            
            Object[] judul_kolom = {"#","Kode Bulu", "Judul Buku", "Tahun Terbit", "Penulis"};
            tabModel=new DefaultTableModel(null,judul_kolom);
            tbBuku.setModel(tabModel);
            
            Connection conn=(Connection)KoneksiDB.koneksiDB();
            Statement stt=conn.createStatement();
            tabModel.getDataVector().removeAllElements();
            
            RsMapel=stt.executeQuery("SELECT * from tb_buku");  
            while(RsMapel.next()){
                Object[] data={
                    RsMapel.getString("id"),
                    RsMapel.getString("kode_buku"),
                    RsMapel.getString("judul_buku"),  
                    RsMapel.getString("tahun_terbit"),
                    RsMapel.getString("penulis"),
                };
                tabModel.addRow(data);
                
                total++;
            }                
            
            totBuku.setText("Total Buku: "+Integer.toString(total)+" Data");
            
        } catch (SQLException ex) {
            System.err.println(ex.getMessage());
        }
        
        btnEdit.setEnabled(false);
        btnHapus.setEnabled(false);
    }
    
